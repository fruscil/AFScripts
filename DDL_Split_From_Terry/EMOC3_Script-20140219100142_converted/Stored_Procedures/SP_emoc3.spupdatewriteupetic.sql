CREATE PROCEDURE         "EMOC3"."SPUPDATEWRITEUPETIC" (

AIRCRAFTIDIN WRITEUPS.AIRCRAFTID%TYPE,
ETICIN WRITEUPS.ETIC%TYPE

)
AS
BEGIN
UPDATE WRITEUPS
SET

WRITEUPS.ETIC = ETICIN

WHERE ( (WRITEUPS.AIRCRAFTID = AIRCRAFTIDIN)
	  	AND (PACER=1)   );


END SPUPDATEWRITEUPETIC;

 
 
 
 
 
 
 
 
