CREATE PROCEDURE         "EMOC3"."SPDELETEMEVENT" (
AIRCRAFTIDIN MAINTENANCEEVENTS.AIRCRAFTID%TYPE,
JCNIN MAINTENANCEEVENTS.EVENTID%TYPE
)
AS
BEGIN

DELETE FROM MAINTENANCEEVENTS

WHERE AIRCRAFTID = AIRCRAFTIDIN
AND EVENTID = JCNIN;

END SPDELETEMEVENT;

 
 
 
 
 
 
 
 
